                CREATE OR REPLACE FUNCTION md5 (valor varchar) 
                return varchar2 is
                    v_input varchar2(2000) := valor;
                    v_input_cripto varchar2(32) := null;
                        begin
                            v_input_cripto := rawtohex(dbms_obfuscation_toolkit.md5(input => utl_raw.cast_to_raw(v_input)));
